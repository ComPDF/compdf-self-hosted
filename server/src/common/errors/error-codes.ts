export function numericCodeForServerError(errorCode: string, status: number): number {
  return SERVER_ERROR_CODE_TO_NUMERIC[errorCode] ?? numericCodeForHttpStatus(status);
}

function numericCodeForHttpStatus(status: number): number {
  switch (status) {
    case 400: return 100001;
    case 401: return 140001;
    case 403: return 140002;
    case 404: return 150001;
    case 409: return 150003;
    case 408:
    case 504: return 190006;
    case 413: return 100104;
    case 429: return 130012;
    default: return 190999;
  }
}
